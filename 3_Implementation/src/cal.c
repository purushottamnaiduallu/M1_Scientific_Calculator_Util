#include<stdio.h>

// add function
float add(float num1,float num2)
{
return num1+num2;
}
// sub function
float sub(float num1,float num2)
{
return num1-num2;
}

// product function
float product(float num1,float num2)
{
return num1*num2;
}

// div function
float div(float num1,float num2)
{
return num1 / num2;
}

// sqr function
